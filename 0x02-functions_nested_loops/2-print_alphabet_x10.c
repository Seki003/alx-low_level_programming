#include "main.h"

/**
 * 2-print_alphabet_x10 - printing alphabets 10 times
 * 
 * Return to a new ligne
*/

void print_alphabet_x10(void)
{
    int i;
    char c;

    for (i = 10; i <= 10; i++)
    {
        for (c = 'a';c <= 'z'; c++)
        {
            _putchar(c);
        }
        _putchar('\n');
    }

}
